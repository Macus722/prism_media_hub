    function switchTab(platform) {
        // 1. Reset all Tabs Styling
        document.querySelectorAll('.platform-tab').forEach(tab => {
            tab.classList.remove('border-[#00ffff]', 'bg-[#00ffff]/10', 'text-white');
            tab.classList.add('border-transparent', 'text-gray-500');
        });

        // 2. Hide all Content
        document.querySelectorAll('.platform-content').forEach(content => {
            content.classList.add('hidden');
            content.classList.remove('block');
        });

        // 3. Activate Selected Tab
        const activeTab = document.getElementById('tab-' + platform);
        activeTab.classList.remove('border-transparent', 'text-gray-500');
        activeTab.classList.add('border-[#00ffff]', 'bg-[#00ffff]/10', 'text-white');

        // 4. Show Selected Content
        const activeContent = document.getElementById('content-' + platform);
        activeContent.classList.remove('hidden');
        activeContent.classList.add('block');
    }
</script>

